package com.zkhk.entity;
/**
 * 问题答案对象
 * @author bit
 *
 */
public class Mfq11 {

	private int problemId;//问题ID
	
	private int ansId;//答案ID
	
	private String description;//答案内容描述
	
	private String mark;//答案标号:A ,B,C或1,2,3
	
	private String fillBlank;//如果是填空，该项不为空，否则为空
	
	private String fillTag;//Y标示填空
	
	private String relatedIds;// 答案关联的问题的id

	public String getRelatedIds() {
		return relatedIds;
	}

	public void setRelatedIds(String relatedIds) {
		this.relatedIds = relatedIds;
	}

	/*  每个答案选项对应的分值   */
	private double answerScore;

	public int getProblemId() {
		return problemId;
	}

	public void setProblemId(int problemId) {
		this.problemId = problemId;
	}

	public int getAnsId() {
		return ansId;
	}

	public void setAnsId(int ansId) {
		this.ansId = ansId;
	}

	public String getDescription() {
		return description;
	}

	public void setDescription(String description) {
		this.description = description;
	}

	public String getMark() {
		return mark;
	}

	public void setMark(String mark) {
		this.mark = mark;
	}

	public String getFillBlank() {
		return fillBlank;
	}

	public void setFillBlank(String fillBlank) {
		this.fillBlank = fillBlank;
	}

	public String getFillTag() {
		return fillTag;
	}

	public void setFillTag(String fillTag) {
		this.fillTag = fillTag;
	}

    public double getAnswerScore() {
        return answerScore;
    }

    public void setAnswerScore(double answerScore) {
        this.answerScore = answerScore;
    }
	
	
}
