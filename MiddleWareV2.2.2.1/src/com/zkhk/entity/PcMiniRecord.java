package com.zkhk.entity;

import java.io.Serializable;

/**
 * @ClassName:     PcMiniRecord.java 
 * @Description:   pc端发送mini记录
 * @author         liuxiaoqin  
 * @version        V1.0   
 * @Date           2016年1月25日 下午4:13:46
*****/
public class PcMiniRecord implements Serializable{
    
	/**
    * 
    */
    private static final long serialVersionUID = -8774349875169286051L;
    
    /*  用户id  */
    private String userId;
    
    /*  用户名   */
	private String memName;
	
	/*  性别：”F”:”女”;”M”:”男”  */
	private String gender;
	
	/*  手机号码  */
	private String tel;
	
	/*  身份证  */
	private String idCard;
	
	/*  蓝牙设备地址  */
	private String bluetoothMacAddr;
	
	/*  mini下放时间('yyyy-mm-dd hh:mm:ss')  */
	private String sendDownTime;
	
	/*  mini上传时间('yyyy-mm-dd hh:mm:ss')  */
	private String uploadTime;
	
	/*  是否已经上传 "0":"未上传","1":"已上传"  */
	private int hasUploaded;
	
	/*  是否已经 "0":"未删除","1":"已删除"  */
    private int hasDeleted;
    
    /*  分页查询符合条件的总记录数  */
    private int totalRecord;
    
    /*  mini下发的医生的id  */
    private int doctorId;

    public String getUserId() {
        return userId;
    }

    public void setUserId(String userId) {
        this.userId = userId;
    }

    public String getMemName() {
        return memName;
    }

    public void setMemName(String memName) {
        this.memName = memName;
    }

    public String getGender() {
        return gender;
    }

    public void setGender(String gender) {
        this.gender = gender;
    }

    public String getTel() {
        return tel;
    }

    public void setTel(String tel) {
        this.tel = tel;
    }

    public String getIdCard() {
        return idCard;
    }

    public void setIdCard(String idCard) {
        this.idCard = idCard;
    }

    public String getBluetoothMacAddr() {
        return bluetoothMacAddr;
    }

    public void setBluetoothMacAddr(String bluetoothMacAddr) {
        this.bluetoothMacAddr = bluetoothMacAddr;
    }

    public String getSendDownTime() {
        return sendDownTime;
    }

    public void setSendDownTime(String sendDownTime) {
        this.sendDownTime = sendDownTime;
    }

    public String getUploadTime() {
        return uploadTime;
    }

    public void setUploadTime(String uploadTime) {
        this.uploadTime = uploadTime;
    }

    public int getHasUploaded() {
        return hasUploaded;
    }

    public void setHasUploaded(int hasUploaded) {
        this.hasUploaded = hasUploaded;
    }

    public int getHasDeleted() {
        return hasDeleted;
    }

    public void setHasDeleted(int hasDeleted) {
        this.hasDeleted = hasDeleted;
    }

    public int getTotalRecord() {
        return totalRecord;
    }

    public void setTotalRecord(int totalRecord) {
        this.totalRecord = totalRecord;
    }

	public int getDoctorId() {
		return doctorId;
	}

	public void setDoctorId(int doctorId) {
		this.doctorId = doctorId;
	}
    
}
