package com.zkhk.entity;

/**
 * 会员档案信息查询2接口参数对象
 * @author bit
 *
 */
public class RecordParam {

	private int memberId; //会员ID
	
	private String measureType;//测量类型:ECG,PPG,BP,BS

	private String measureStart;//测量起始时间格式如：20141021143021

	private String measureEnd;//测量起始时间格式如：20141021143021


	public int getMemberId() {
		return memberId;
	}

	public void setMemberId(int memberId) {
		this.memberId = memberId;
	}

	public String getMeasureType() {
		return measureType;
	}

	public void setMeasureType(String measureType) {
		this.measureType = measureType;
	}

	public String getMeasureStart() {
		return measureStart;
	}

	public void setMeasureStart(String measureStart) {
		this.measureStart = measureStart;
	}

	public String getMeasureEnd() {
		return measureEnd;
	}

	public void setMeasureEnd(String measureEnd) {
		this.measureEnd = measureEnd;
	}
	
	
	
	
}
